CubeMesh::CubeMesh()
{
	setupMesh(Type::pos);
}

CubeMesh::CubeMesh(std::shared_ptr<Texture> texture)
{
	textures.push_back(texture);
	setupMesh(Type::posNormalUV);
}

CubeMesh::CubeMesh(std::vector<std::shared_ptr<Texture>> textures)
	: textures(textures)
{
	setupMesh(Type::posNormalUV);
}

CubeMesh::~CubeMesh()
{
	glDeleteVertexArrays(1, &vao);
	glDeleteBuffers(1, &_vbo);
}

void CubeMesh::draw(Shader shader, bool isUsingInternalTexture /*= true*/)
{
	if (isUsingInternalTexture)
	{
		for (unsigned int i = 0; i < textures.size(); ++i)
		{
			auto tex = textures[i];

			// set the sampler to the correct texture unit
			shader.setInt(tex->getSamplerName(), i);
			tex->bind(GL_TEXTURE0 + i);
		}
	}

	//draw mesh
	glBindVertexArray(vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);

	// always good practice to set everything back to defaults once configured.
	glActiveTexture(GL_TEXTURE0);
}

